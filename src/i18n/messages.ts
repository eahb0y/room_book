const interpolate = (value: string, params?: Record<string, string | number>) => {
  if (!params) return value;
  return value.replace(/\{(\w+)\}/g, (_match, key: string) => {
    const paramValue = params[key];
    return paramValue === undefined ? `{${key}}` : String(paramValue);
  });
};

export const localeLabels: Record<AppLocale, string> = {
  ru: 'RU',
  uz: 'UZ',
};

export const getDateLocale = (locale: AppLocale): DateFnsLocale => (locale === 'uz' ? uz : ru);

export const getIntlLocale = (locale: AppLocale) => (locale === 'uz' ? 'uz-UZ' : 'ru-RU');

export const translate = (
  value: string,
  locale: AppLocale,
  params?: Record<string, string | number>,
) => {
  if (locale === 'ru') {
    return interpolate(value, params);
  }

  const translated = uzTranslations[value] ?? value;
  return interpolate(translated, params);
};
